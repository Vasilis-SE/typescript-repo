"use strict";
// Basic operations
console.log('\n ----- Basic operations -------\n');
console.log(12 + 53);
console.log(12 - 53);
console.log(12 * 53);
console.log(12 / 53);
// Relational operations
console.log('\n ----- Relational operations -------\n');
console.log('12 > 53', (12 > 53));
console.log('12 >= 53', (12 >= 53));
console.log('12 < 53', (12 < 53));
console.log('12 <= 53', (12 <= 53));
console.log('true == true', (true == true));
console.log('false != false', (false != false));
// Logical operations
console.log('\n ----- Logical operations -------\n');
console.log('(12 > 53 && 14 <= 12)', (12 > 53 && 14 <= 12));
console.log('(12 > 53 || 14 <= 12)', (12 > 53 || 14 <= 12));
console.log('!(12 > 53 && 14 <= 12)', !(12 > 53 && 14 <= 12));
// Bitwise operators
console.log('\n ----- Bitwise operators -------\n');
console.log('(1 & 0)', (1 & 0));
console.log('(1 | 0)', (1 | 0));
console.log('(1 ^ 0)', (1 ^ 0));
console.log('(1 << 0)', (1 << 0));
console.log('(1 >> 0)', (1 >> 0));
console.log('(1 >>> 0)', (1 >>> 0));
